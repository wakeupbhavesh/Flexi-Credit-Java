
public class ArithmeticOperation {
    private int a;
    private int b;
    private int c;
    public void addition(int p1,int p2)
    {
    	a = p1;
    	b = p2;
    	c = a+b;
    	System.out.println(" addition is : "+c);
    }
    public void subtraction(int p1,int p2)
    {
    	a = p1;
    	b = p2;
    	c = a-b;
    	System.out.println(" subtraction is : "+c);
    }
}
